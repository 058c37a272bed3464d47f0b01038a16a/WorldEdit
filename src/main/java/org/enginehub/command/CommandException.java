package org.enginehub.command;

/**
 * Throw an exception related to command handler.
 *
 * <p>Messages stored in these exceptions should be user-friendly.</p>
 */
public class CommandException extends Exception {

    private static final long serialVersionUID = -1305187256508870606L;

    public CommandException() {
    }

    public CommandException(String message) {
        super(message);
    }

    public CommandException(String message, Throwable cause) {
        super(message, cause);
    }

    public CommandException(Throwable cause) {
        super(cause);
    }

}
